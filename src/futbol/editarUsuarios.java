    private void tablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaUsuariosMouseClicked
        // TODO add your handling code here:
        int fila = tablaUsuarios.getSelectedRow();
        if (fila==-1) {
            JOptionPane.showMessageDialog(null, "USUARIO NO SELLECCIONADO");
            
        }else{
            id=Integer.parseInt((String)tablaUsuarios.getValueAt(fila,0).toString());
            String nom =(String)tablaUsuarios.getValueAt(fila,1);
            String app =(String)tablaUsuarios.getValueAt(fila,2);
            String password =(String)tablaUsuarios.getValueAt(fila,3);
            String cargo =(String)tablaUsuarios.getValueAt(fila,4);
            String email =(String)tablaUsuarios.getValueAt(fila,5);
            ID.setText(""+id);
            NOMBRE.setText(nom);
            APELLIDO.setText(app);
            EMAIL.setText(email);
            PASSWORD.setText(password);
            CARGO.setText(cargo);
        }
    }//GEN-LAST:event_tablaUsuariosMouseClicked

