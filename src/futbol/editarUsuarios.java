         void listar(){
        String sql=("select * from usuarios");
        try{
            cn = con.conectar();
            st =cn.createStatement();
            rs=st.executeQuery(sql);
            Object[]usuarios = new Object[6];
            modelo =(DefaultTableModel)tablaUsuarios.getModel();
            while(rs.next()){
                usuarios[0]=rs.getInt("id_usuario");
                usuarios[1]=rs.getString("nombre_us");
                usuarios[2]=rs.getString("apellido_us");
                usuarios[3]=rs.getString("contrasena");  
                usuarios[4]=rs.getString("privilegio");
                usuarios[5]=rs.getString("email");

                modelo.addRow(usuarios);
            }
            tablaUsuarios.setModel(modelo);
        }catch (Exception e){
        
        }
    }
         void editar(){
             String nombre_us=NOMBRE.getText();
             String apellido_us=APELLIDO.getText();
             String contrasena=PASSWORD.getText();
             String privilegio=CARGO.getText();
             String email=EMAIL.getText();
             String sql="update usuarios set nombre_us='"+nombre_us+"',apellido_us='"+apellido_us+"',contrasena='"+contrasena+"', privilegio='"+privilegio+"', email='"+email+"'where id_usuario="+id;
             if (nombre_us.equals("") || apellido_us.equals("") || contrasena.equals("") || privilegio.equals("") || email.equals("")) {
                JOptionPane.showMessageDialog(null, "DEBE INGRSAR DATOS");
             }else{
                     try{
                        cn=con.getConcection();
                        st=cn.createStatement();
                        st.executeUpdate(sql);
                        JOptionPane.showMessageDialog(null, "USUARIO ACTUALIZADO");
                        limpiarTabla(modelo);
                    }catch(Exception e){
                 
                 }
             }
         }
         
         void eliminar(){
             int filaSeleccion = tablaUsuarios.getSelectedRow();
             if (filaSeleccion==1) {
                 JOptionPane.showMessageDialog(null,"DEBE SELECCIONAR UNA FILA");
             }else{
                 String sql ="delete from usuarios where id_usuario="+id;
                 try{
                     cn= con.getConcection();
                     st=cn.createStatement();
                     st.executeLargeUpdate(sql);
                     JOptionPane.showMessageDialog(null,"USUARIO ELIMINADO");
                     limpiarTabla(modelo);
                 }catch(Exception e){
                 
                 }
             }
         }
