    private void inicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inicioActionPerformed
        // TODO add your handling code here:
        String usuario= USUARIO.getText();
        String pass= PASSWORD.getText();
        if (!usuario.equals("") || !pass.equals("")) {
            try{
                PreparedStatement ps = cn.prepareStatement("SELECT privilegio FROM usuarios WHERE email='"+usuario+"' AND contrasena='"+pass+"'");
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    String privilegio=rs.getString("privilegio");
                    if (privilegio.equalsIgnoreCase("ADMIN")) {
                        dispose();
                        adminMenu pg = new adminMenu();
                        pg.setVisible(true);
                    }else if(privilegio.equalsIgnoreCase("SECRETARIO")){
                        dispose();
                        menuSecretario pc=new menuSecretario();
                        pc.setVisible(true);
                    }
                }else{
                    JOptionPane.showMessageDialog(null,"USUARIO O CONTRASEÃ‘A INCORRECTOS");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,"ERROR AL INICIAR CESION"+e);
            }
        }else{
        JOptionPane.showMessageDialog(null,"DEBE INGRESAR DATOS");
        }
    }//GEN-LAST:event_inicioActionPerformed

